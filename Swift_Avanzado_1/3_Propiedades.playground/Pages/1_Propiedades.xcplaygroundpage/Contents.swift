//: # Propiedades en Swift

/*:
 Las propiedades en Swift son características asociadas a una clase, estructura o enumerador. Estas propiedades permiten almacenar y gestionar datos de forma estructurada. Existen varios tipos de propiedades que se utilizan según las necesidades específicas del diseño.
 
 ## Tipos de Propiedades
 1. **Stored Properties**: Almacenan valores como parte de una instancia.
 2. **Lazy Properties**: Se inicializan solo cuando se accede a ellas por primera vez.
 3. **Computed Properties**: No almacenan valores, sino que proporcionan un getter y, opcionalmente, un setter.
 4. **Property Observers**: Observan cambios en las propiedades almacenadas y permiten ejecutar código al modificarlas.
 5. **Type Properties**: Propiedades que pertenecen a la clase o estructura en lugar de a instancias individuales.
 */

