/*:
 # Métodos en Swift

Un **método** es una función que pertenece a una clase, estructura o enumeración en Swift. Los métodos definen el comportamiento de estos tipos de datos y pueden interactuar con sus propiedades.

## Tipos de Métodos

1. **Métodos de Instancia**: Operan sobre instancias específicas, accediendo y modificando sus propiedades mediante `self`. Por ejemplo, `self.propiedad` hace referencia a la propiedad de la instancia actual.

2. **Métodos de Clase o de Tipo**: Se definen con `static` o `class` y operan a nivel de clase o estructura, sin necesidad de instanciar un objeto.

3. **Métodos Mutables**: Permiten modificar las propiedades de una instancia. Para acceder a las propiedades dentro de estos métodos, también se utiliza `self`.

La estructura de métodos favorece un código modular y organizado, facilitando su mantenimiento y reutilización.
